package javapackage;

public class Variable1 {

	public static void main(String[] args) {
		int a=100;
		System.out.println(a);
		int x=100;
		System.out.println(x);
		x=200;
		System.out.println(x);

		char c ='a';
		System.out.println(c);
		
		
		boolean b=false;
		b=true;
		System.out.println(b);
		int e=150;
		int f=120;
		boolean z=e<f;
		System.out.println(z);
		
	}

}
